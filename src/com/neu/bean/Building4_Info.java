package com.neu.bean;

public class Building4_Info {
	
	String com_name = null;
	String build_num = null;
	String unit_num = null;
	String room_num = null;
	String de_type = null;
	String date = null;
	
	public String getCom_name() {
		return com_name;
	}
	public void setCom_name(String comName) {
		com_name = comName;
	}
	public String getBuild_num() {
		return build_num;
	}
	public void setBuild_num(String buildNum) {
		build_num = buildNum;
	}
	public String getUnit_num() {
		return unit_num;
	}
	public void setUnit_num(String unitNum) {
		unit_num = unitNum;
	}
	public String getRoom_num() {
		return room_num;
	}
	public void setRoom_num(String roomNum) {
		room_num = roomNum;
	}
	public String getDe_type() {
		return de_type;
	}
	public void setDe_type(String deType) {
		de_type = deType;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}

}
