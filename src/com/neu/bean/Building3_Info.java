package com.neu.bean;

public class Building3_Info {

	String com_name;
	String build_num;
	String unit_num;
	String room_num;
	String owner_name;
	int owner_num;
	int population;
	String date;
	
	public String getCom_name() {
		return com_name;
	}
	public void setCom_name(String comName) {
		com_name = comName;
	}
	public String getBuild_num() {
		return build_num;
	}
	public void setBuild_num(String buildNum) {
		build_num = buildNum;
	}
	public String getUnit_num() {
		return unit_num;
	}
	public void setUnit_num(String unitNum) {
		unit_num = unitNum;
	}
	public String getRoom_num() {
		return room_num;
	}
	public void setRoom_num(String roomNum) {
		room_num = roomNum;
	}
	public String getOwner_name() {
		return owner_name;
	}
	public void setOwner_name(String ownerName) {
		owner_name = ownerName;
	}
	public int getOwner_num() {
		return owner_num;
	}
	public void setOwner_num(int ownerNum) {
		owner_num = ownerNum;
	}
	public int getPopulation() {
		return population;
	}
	public void setPopulation(int population) {
		this.population = population;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
