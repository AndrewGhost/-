package com.neu.bean;

public class Building2_Info {
	
	String com_name = null;
	String build_num = null;
	String period = null;
	int floor_amount = 0;
	int unit_amount = 0;
	int room_amount = 0;
	
	public String getCom_name() {
		return com_name;
	}
	public void setCom_name(String comName) {
		com_name = comName;
	}
	public String getBuild_num() {
		return build_num;
	}
	public void setBuild_num(String buildNum) {
		build_num = buildNum;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public int getFloor_amount() {
		return floor_amount;
	}
	public void setFloor_amount(int floorAmount) {
		floor_amount = floorAmount;
	}
	public int getUnit_amount() {
		return unit_amount;
	}
	public void setUnit_amount(int unitAmount) {
		unit_amount = unitAmount;
	}
	public int getRoom_amount() {
		return room_amount;
	}
	public void setRoom_amount(int roomAmount) {
		room_amount = roomAmount;
	}
	

}
